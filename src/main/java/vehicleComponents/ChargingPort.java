package vehicleComponents;

public class ChargingPort {

    private int wattage;

    public ChargingPort(int wattage) {
        this.wattage = wattage;
    }

    public int getWattage() {
        return wattage;
    }

    public void setWattage(int wattage) {
        this.wattage = wattage;
    }
}
